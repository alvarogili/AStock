    /**
     * Constructor
     * @param thread thread a ejecutar para el procesamiento
     * @param text Texto a escribir en el dialogo
     */
    public Procesando(Thread thread, String text) {
        initComponents();
        jLabelMensaje.setText(text);
        this.thread = thread;
        if (thread instanceof GenerarBackupThread) {
            ((GenerarBackupThread) thread).setProcesando(this);
        }else if(thread instanceof RestaurarBackupThread){
            ((RestaurarBackupThread) thread).setProcesando(this);
        }
    }

    /**
     * Constructor
     * @param text Texto a escribir en el dialogo
     */
    public Procesando(String text){
        initComponents();
        jLabelMensaje.setText(text);
    }

    public void mostrar(boolean show, JFrame parent) {                

        if (show) {
            if(thread != null)
                thread.start();
            this.setLocationRelativeTo(parent);
            this.setAlwaysOnTop(true);
            this.parent = parent;
        }        
        this.setModal(show);
        this.setVisible(show);
    }


