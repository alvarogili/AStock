    private void close() {
        if (JOptionPane.showConfirmDialog(this, "¿Realmente desea cerrar AStock?",
                "Confirmación", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE,
                new ImageIcon(getClass().getResource("/img/svn_status.png"))) == JOptionPane.OK_OPTION) {
            try {
                persistenceManager.close();
            } catch (Exception e) {
            }

            System.exit(0);
        }
    }
