    public void mostrarAlertaEventosDia(final AStock aStock) {

        SwingUtilities.invokeLater(new Runnable() {

            @Override
            public void run() {
                if (JOptionPane.showConfirmDialog(aStock, "Existen Eventos para hoy, ¿Desea ver su agenda?",
                        "Alerta agenda", JOptionPane.OK_CANCEL_OPTION,
                        JOptionPane.INFORMATION_MESSAGE,
                        new ImageIcon(getClass().getResource("/img/important.png"))) == JOptionPane.OK_OPTION) {

                    //muestro la agenda
                    MediadorAgenda agenda = MediadorAgenda.getInstance();
                }
            }
        });
    }

    public void mostrarAlertaEvento(final AStock aStock, final Evento evento) {

        SwingUtilities.invokeLater(new Runnable() {

            @Override
            public void run() {
                AlertaEventoDialog aed = new AlertaEventoDialog(aStock, true);
                aed.setEvento(evento);
                aed.setLocationRelativeTo(aStock);
                aed.show();
            }
        });
    }

